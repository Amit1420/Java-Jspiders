package com.amit.javaprogrampack;

public class Demo4 {

	public static void main(String[] args) {
		int a=10,b=5;
		calculation(a,b);

	}
	public static void calculation(int a,int b)
	{
		System.out.println(a+" + "+b+" = "+(a+b));
		System.out.println(a+" - "+b+" = "+(a-b));
		System.out.println(a+" * "+b+" = "+(a*b));
		System.out.println(a+" / "+b+" = "+(a/b));
		
		
	}

}
