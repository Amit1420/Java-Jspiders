package com.amit.stringprogramspack;


public class ReplaceCharAndString {

	public static void main(String[] args) {
		String s="java is awesome language";
		System.out.println(s.replace("awesome", "easy"));
		System.out.println(s.replace('a', '@'));
		System.out.println(s.replace("a", ""));
	

	}

}
